// scripts/build-table-columns.js
// Parses src/types/supabase.ts and produces src/lib/repository/tableColumns.ts
// with explicit column lists for every public table.
const fs = require('fs');
const path = require('path');

const TYPES_PATH = path.join(__dirname, '..', 'src', 'types', 'supabase.ts');
const OUT_PATH = path.join(__dirname, '..', 'src', 'lib', 'repository', 'tableColumns.ts');

const src = fs.readFileSync(TYPES_PATH, 'utf8');

const tablesIdx = src.indexOf('Tables: {');
if (tablesIdx < 0) {
  console.error('Tables: { not found');
  process.exit(1);
}

const tableMap = {};
let i = tablesIdx + 'Tables: {'.length;
const len = src.length;
let depth = 1;

while (i < len && depth > 0) {
  const ch = src[i];
  if (ch === '{') {
    depth++;
    i++;
    continue;
  }
  if (ch === '}') {
    depth--;
    i++;
    continue;
  }
  if (ch === "'" || ch === '"' || ch === '`') {
    const quote = ch;
    i++;
    while (i < len && src[i] !== quote) {
      if (src[i] === '\\') i++;
      i++;
    }
    i++;
    continue;
  }
  if (depth === 1) {
    const m = src.slice(i).match(/^([a-zA-Z_][a-zA-Z0-9_]*)\s*:\s*\{/);
    if (m) {
      const tableName = m[1];
      i += m[0].length;
      const tableStart = i;
      let tableDepth = 1;
      while (i < len && tableDepth > 0) {
        const c = src[i];
        if (c === '{') tableDepth++;
        else if (c === '}') tableDepth--;
        else if (c === "'" || c === '"' || c === '`') {
          const q = c;
          i++;
          while (i < len && src[i] !== q) {
            if (src[i] === '\\') i++;
            i++;
          }
        }
        i++;
      }
      const tableBlock = src.slice(tableStart, i - 1);
      const rowMatch = tableBlock.match(/Row:\s*\{/);
      if (rowMatch) {
        const rowStart = rowMatch.index + rowMatch[0].length;
        let d = 1;
        let j = rowStart;
        while (j < tableBlock.length && d > 0) {
          const c = tableBlock[j];
          if (c === '{') d++;
          else if (c === '}') d--;
          else if (c === "'" || c === '"' || c === '`') {
            const q = c;
            j++;
            while (j < tableBlock.length && tableBlock[j] !== q) {
              if (tableBlock[j] === '\\') j++;
              j++;
            }
          }
          j++;
        }
        const rowBlock = tableBlock.slice(rowStart, j - 1);
        const cols = [];
        const colRegex = /^\s*([a-zA-Z_][a-zA-Z0-9_]*)\s*:/gm;
        let cm;
        while ((cm = colRegex.exec(rowBlock)) !== null) {
          cols.push(cm[1]);
        }
        if (cols.length > 0) tableMap[tableName] = cols;
      }
      continue;
    }
  }
  i++;
}

const lines = [];
lines.push('// AUTO-GENERATED by scripts/build-table-columns.js — do not edit.');
lines.push('// Source: src/types/supabase.ts Tables.<name>.Row');
lines.push('// Used by repositories to never write `.select("*")`.');
lines.push('');
lines.push('export const TABLE_COLUMNS = {');
for (const [tbl, cols] of Object.entries(tableMap).sort()) {
  lines.push(`  ${tbl}: [${cols.map((c) => `'${c}'`).join(', ')}] as const,`);
}
lines.push('} as const;');
lines.push('');
lines.push('export type TableName = keyof typeof TABLE_COLUMNS;');
lines.push('');
lines.push('export function tableColumns(name: TableName): string {');
lines.push('  return (TABLE_COLUMNS[name] as readonly string[]).join(", ");');
lines.push('}');
lines.push('');

fs.mkdirSync(path.dirname(OUT_PATH), { recursive: true });
fs.writeFileSync(OUT_PATH, lines.join('\n'), 'utf8');
console.log(`Wrote ${Object.keys(tableMap).length} table column lists to ${path.relative(process.cwd(), OUT_PATH)}`);
