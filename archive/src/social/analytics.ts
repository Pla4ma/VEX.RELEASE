/**
 * Social System - Analytics
 *
 * Comprehensive analytics tracking for social features.
 */

import { getAnalyticsService } from '../analytics/AnalyticsService';
import { eventBus } from '../events';
import { createDebugger } from '../utils/debug';
import type { SocialPost, SocialUser, ReactionType, FriendRelationship } from './types';

const debug = createDebugger('social:analytics');

// Analytics event types
export type SocialAnalyticsEvent =
  | 'social_post_created'
  | 'social_post_viewed'
  | 'social_post_shared'
  | 'social_post_deleted'
  | 'social_reaction_added'
  | 'social_reaction_removed'
  | 'social_comment_added'
  | 'social_friend_request_sent'
  | 'social_friend_request_accepted'
  | 'social_friend_removed'
  | 'social_follow_added'
  | 'social_follow_removed'
  | 'social_profile_viewed'
  | 'social_feed_refreshed'
  | 'social_feed_load_more'
  | 'social_error_occurred'
  | 'social_offline_mode_activated'
  | 'social_degraded_mode_activated';

// Event properties
interface SocialEventProperties {
  user_id?: string;
  post_id?: string;
  post_type?: string;
  reaction_type?: string;
  target_user_id?: string;
  friend_count?: number;
  follower_count?: number;
  following_count?: number;
  feed_size?: number;
  from_cache?: boolean;
  error_code?: string;
  queue_size?: number;
  [key: string]: unknown;
}

/**
 * Track a social analytics event
 */
export function trackSocialEvent(
  event: SocialAnalyticsEvent,
  userId: string,
  properties?: Partial<SocialEventProperties>
): void {
  const analytics = getAnalyticsService();

  const enrichedProperties: SocialEventProperties = {
    user_id: userId,
    timestamp: Date.now(),
    ...properties,
  };

  analytics.track(event, enrichedProperties);

  // Also publish to event bus for cross-system integration
  eventBus.publish('analytics:track', {
    event,
    properties: enrichedProperties,
  });

  debug.debug(`Tracked ${event}`, enrichedProperties);
}

/**
 * Track post creation
 */
export function trackPostCreated(
  userId: string,
  post: SocialPost,
  autoGenerated: boolean = false
): void {
  trackSocialEvent('social_post_created', userId, {
    post_id: post.id,
    post_type: post.type,
    has_media: post.mediaUrls && post.mediaUrls.length > 0,
    is_public: post.isPublic,
    auto_generated: autoGenerated,
  });
}

/**
 * Track post view
 */
export function trackPostViewed(
  viewerId: string,
  post: SocialPost
): void {
  trackSocialEvent('social_post_viewed', viewerId, {
    post_id: post.id,
    post_type: post.type,
    post_owner_id: post.userId,
  });
}

/**
 * Track reaction
 */
export function trackReactionAdded(
  userId: string,
  postId: string,
  reaction: ReactionType,
  postOwnerId: string
): void {
  trackSocialEvent('social_reaction_added', userId, {
    post_id: postId,
    reaction_type: reaction,
    target_user_id: postOwnerId,
  });
}

/**
 * Track reaction removal
 */
export function trackReactionRemoved(
  userId: string,
  postId: string,
  reaction: ReactionType
): void {
  trackSocialEvent('social_reaction_removed', userId, {
    post_id: postId,
    reaction_type: reaction,
  });
}

/**
 * Track friend request
 */
export function trackFriendRequestSent(
  fromUserId: string,
  toUserId: string
): void {
  trackSocialEvent('social_friend_request_sent', fromUserId, {
    target_user_id: toUserId,
  });
}

/**
 * Track friend request acceptance
 */
export function trackFriendRequestAccepted(
  accepterId: string,
  requesterId: string,
  relationship: FriendRelationship
): void {
  trackSocialEvent('social_friend_request_accepted', accepterId, {
    target_user_id: requesterId,
    friend_count: relationship ? 1 : undefined, // Would need actual count
  });
}

/**
 * Track profile view
 */
export function trackProfileViewed(
  viewerId: string,
  profileId: string,
  profile?: SocialUser
): void {
  trackSocialEvent('social_profile_viewed', viewerId, {
    target_user_id: profileId,
    profile_level: profile?.level,
    profile_streak: profile?.currentStreak,
  });
}

/**
 * Track feed refresh
 */
export function trackFeedRefreshed(
  userId: string,
  itemCount: number,
  fromCache: boolean
): void {
  trackSocialEvent('social_feed_refreshed', userId, {
    feed_size: itemCount,
    from_cache: fromCache,
  });
}

/**
 * Track feed load more
 */
export function trackFeedLoadMore(
  userId: string,
  itemCount: number,
  hasMore: boolean
): void {
  trackSocialEvent('social_feed_load_more', userId, {
    feed_size: itemCount,
    has_more: hasMore,
  });
}

/**
 * Track error
 */
export function trackSocialError(
  userId: string,
  errorCode: string,
  context: string,
  recoverable: boolean
): void {
  trackSocialEvent('social_error_occurred', userId, {
    error_code: errorCode,
    context,
    recoverable,
  });
}

/**
 * Track offline mode
 */
export function trackOfflineModeActivated(
  userId: string,
  queueSize: number
): void {
  trackSocialEvent('social_offline_mode_activated', userId, {
    queue_size: queueSize,
  });
}

/**
 * Track degraded mode
 */
export function trackDegradedModeActivated(
  userId: string,
  reason: string
): void {
  trackSocialEvent('social_degraded_mode_activated', userId, {
    reason,
  });
}

/**
 * Social Analytics class for advanced tracking
 */
export class SocialAnalytics {
  private userId: string;

  constructor(userId: string) {
    this.userId = userId;
  }

  trackPostCreated(post: SocialPost, autoGenerated: boolean = false): void {
    trackPostCreated(this.userId, post, autoGenerated);
  }

  trackPostViewed(post: SocialPost): void {
    trackPostViewed(this.userId, post);
  }

  trackReactionAdded(postId: string, reaction: ReactionType, postOwnerId: string): void {
    trackReactionAdded(this.userId, postId, reaction, postOwnerId);
  }

  trackReactionRemoved(postId: string, reaction: ReactionType): void {
    trackReactionRemoved(this.userId, postId, reaction);
  }

  trackFriendRequestSent(toUserId: string): void {
    trackFriendRequestSent(this.userId, toUserId);
  }

  trackFriendRequestAccepted(requesterId: string, relationship: FriendRelationship): void {
    trackFriendRequestAccepted(this.userId, requesterId, relationship);
  }

  trackProfileViewed(profileId: string, profile?: SocialUser): void {
    trackProfileViewed(this.userId, profileId, profile);
  }

  trackFeedRefreshed(itemCount: number, fromCache: boolean): void {
    trackFeedRefreshed(this.userId, itemCount, fromCache);
  }

  trackFeedLoadMore(itemCount: number, hasMore: boolean): void {
    trackFeedLoadMore(this.userId, itemCount, hasMore);
  }

  trackError(errorCode: string, context: string, recoverable: boolean): void {
    trackSocialError(this.userId, errorCode, context, recoverable);
  }

  trackOfflineMode(queueSize: number): void {
    trackOfflineModeActivated(this.userId, queueSize);
  }

  trackDegradedMode(reason: string): void {
    trackDegradedModeActivated(this.userId, reason);
  }
}
