/**
 * Social System - Domain Models
 *
 * Comprehensive types for the social feature including:
 * - Social posts and interactions
 * - Friend relationships
 * - Social states and error handling
 * - Analytics events
 */

import type { z } from 'zod';
import type {
  SocialPostSchema,
  SocialUserSchema,
  FriendRelationshipSchema,
  SocialActivitySchema,
  SocialErrorSchema,
  SocialStateSchema,
  ReactionTypeSchema,
  PostTypeSchema,
  FriendshipStatusSchema,
  SocialErrorCodeSchema,
  SocialNetworkStatusSchema,
  SocialActionQueueItemSchema,
  SocialAnalyticsEventSchema,
  FeedFilterSchema,
  PaginationSchema,
} from './schemas';

// Core types derived from schemas
export type SocialPost = z.infer<typeof SocialPostSchema>;
export type SocialUser = z.infer<typeof SocialUserSchema>;
export type FriendRelationship = z.infer<typeof FriendRelationshipSchema>;
export type SocialActivity = z.infer<typeof SocialActivitySchema>;
export type SocialError = z.infer<typeof SocialErrorSchema>;
export type SocialState = z.infer<typeof SocialStateSchema>;
export type ReactionType = z.infer<typeof ReactionTypeSchema>;
export type PostType = z.infer<typeof PostTypeSchema>;
export type FriendshipStatus = z.infer<typeof FriendshipStatusSchema>;
export type SocialErrorCode = z.infer<typeof SocialErrorCodeSchema>;
export type SocialNetworkStatus = z.infer<typeof SocialNetworkStatusSchema>;
export type SocialActionQueueItem = z.infer<typeof SocialActionQueueItemSchema>;
export type SocialAnalyticsEvent = z.infer<typeof SocialAnalyticsEventSchema>;
export type FeedFilter = z.infer<typeof FeedFilterSchema>;
export type Pagination = z.infer<typeof PaginationSchema>;

// Feed types
export interface FeedPage {
  items: SocialPost[];
  nextCursor: number | null;
  hasMore: boolean;
}

export interface FeedOptions {
  limit?: number;
  cursor?: number | null;
  filter?: PostType[];
  sortBy?: 'recent' | 'popular' | 'trending';
}

// Friend-related types
export interface FriendRequest {
  id: string;
  fromUserId: string;
  toUserId: string;
  status: 'pending' | 'accepted' | 'rejected';
  message?: string;
  createdAt: number;
  respondedAt?: number;
}

export interface FriendSuggestion {
  user: SocialUser;
  mutualFriends: number;
  reason: 'mutual_friends' | 'squad_member' | 'similar_activity' | 'recent_interaction';
  score: number;
}

// Social interactions
export interface SocialInteraction {
  id: string;
  userId: string;
  targetUserId: string;
  type: 'follow' | 'unfollow' | 'block' | 'unblock' | 'report';
  createdAt: number;
  metadata?: Record<string, unknown>;
}

// Notification types
export interface SocialNotification {
  id: string;
  userId: string;
  type:
    | 'friend_request'
    | 'friend_accepted'
    | 'reaction_received'
    | 'post_mentioned'
    | 'squad_invite'
    | 'achievement_shared'
    | 'streak_broken';
  title: string;
  body: string;
  data: Record<string, unknown>;
  read: boolean;
  createdAt: number;
}

// Activity feed for a user
export interface UserActivityFeed {
  userId: string;
  activities: SocialActivity[];
  unreadCount: number;
  lastUpdated: number;
}

// Social stats
export interface SocialStats {
  userId: string;
  followerCount: number;
  followingCount: number;
  postCount: number;
  totalReactionsReceived: number;
  streakRank?: number;
  levelRank?: number;
  joinedAt: number;
  lastActiveAt: number;
}

// Degraded mode state
export interface SocialDegradedState {
  enabled: boolean;
  reason: SocialErrorCode;
  availableFeatures: string[];
  unavailableFeatures: string[];
  fallbackData?: Partial<SocialState>;
}

// Service configuration
export interface SocialServiceConfig {
  retryAttempts: number;
  retryDelay: number;
  enableOfflineQueue: boolean;
  maxQueueSize: number;
  enableAnalytics: boolean;
  degradedModeTimeout: number;
}

// Event payload types
export interface SocialPostCreatedEvent {
  post: SocialPost;
  userId: string;
  autoGenerated: boolean;
}

export interface SocialReactionEvent {
  postId: string;
  userId: string;
  reaction: ReactionType;
  previousReaction: ReactionType | null;
  postOwnerId: string;
}

export interface SocialFriendshipEvent {
  relationship: FriendRelationship;
  action: 'requested' | 'accepted' | 'rejected' | 'removed';
  fromUserId: string;
  toUserId: string;
}

export interface SocialErrorEvent {
  error: SocialError;
  recoverable: boolean;
  context: string;
}

// Queue action types
export type QueuedSocialAction =
  | { type: 'create_post'; payload: Omit<SocialPost, 'id' | 'createdAt'> }
  | { type: 'react'; payload: { postId: string; reaction: ReactionType } }
  | { type: 'send_friend_request'; payload: { toUserId: string; message?: string } }
  | { type: 'accept_friend_request'; payload: { requestId: string } }
  | { type: 'follow_user'; payload: { targetUserId: string } }
  | { type: 'unfollow_user'; payload: { targetUserId: string } };

// Cache entry
export interface SocialCacheEntry<T> {
  data: T;
  timestamp: number;
  expiresAt: number;
  stale: boolean;
}

// Repository result types
export interface RepositoryResult<T> {
  success: boolean;
  data?: T;
  error?: SocialError;
  cached?: boolean;
  fromCache?: boolean;
}

export interface PaginatedRepositoryResult<T> extends RepositoryResult<T[]> {
  hasMore: boolean;
  nextCursor?: number | null;
  totalCount?: number;
}

// UI State types
export interface SocialUIState {
  feed: {
    loading: boolean;
    error: SocialError | null;
    refreshing: boolean;
    hasMore: boolean;
  };
  profile: {
    loading: boolean;
    error: SocialError | null;
  };
  notifications: {
    loading: boolean;
    error: SocialError | null;
    unreadCount: number;
  };
  friends: {
    loading: boolean;
    error: SocialError | null;
  };
}

// Analytics event payload
export interface SocialAnalyticsPayload {
  event: SocialAnalyticsEvent;
  timestamp: number;
  userId: string;
  properties: Record<string, unknown>;
  sessionId?: string;
}
