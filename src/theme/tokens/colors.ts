export function getColors(
  mode: "light" | "dark",
  highContrast = false,
): ColorPalette {
  if (mode === "dark") {
    return highContrast ? highContrastDarkColors : darkColors;
  }
  return highContrast ? highContrastLightColors : lightColors;
}

